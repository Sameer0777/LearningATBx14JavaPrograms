package Ex_008_If_Condition;

public class Lab_081_If_ElseIf_Else {
    public static void main(String[] args) {

        int age = 24;
        if( age>24 ){
            System.out.println("Age > "+age);
        }
        else if( age<24 ){
            System.out.println("Age < "+age);
        }
        else{
            System.out.println("Age = "+age);
        }
    }
}
