package Ex_010_For_Loop;

public class Lab_107_GreaterEquals {
    public static void main(String[] args) {

        for (int i = 0; i <= 10 ; i++) {
            System.out.println(i); // 0 to 10 -> loop runs 11 times
        }
        for (int i = 1; i <= 10 ; i++) {
            System.out.println(i); // 1 to 10 -> loop runs 10 times
        }
        for (int i = 0 ;i < 10 ; i++) {
            System.out.println(i);// 0 to 9 -> loop runs 10 times
        }
    }
}
