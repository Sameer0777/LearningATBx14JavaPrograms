package Ex_010_For_Loop;

public class Lab_102_If_For_Loop {
    public static void main(String[] args) {

        for (int sam = 0; sam <= 26; sam++) {
            if(sam>=25){
                System.out.println("Settle Down in Life");
            }
            else {
                System.out.println("Enjoy!!!");
            }
        }
    }
}
