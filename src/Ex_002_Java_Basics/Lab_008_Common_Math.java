package Ex_002_Java_Basics;

public class Lab_008_Common_Math {
    public static void main(String[] args) {

        System.out.println(7+2);
        System.out.println(7-2);
        System.out.println(7*2);
        System.out.println(7/2); // println always prints Integer part and not decimal part, so output here is 3 not 3.5
        System.out.println("7+2");
    }
}
