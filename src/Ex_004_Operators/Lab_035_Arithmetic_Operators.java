package Ex_004_Operators;

public class Lab_035_Arithmetic_Operators {
    public static void main(String[] args) {

        /*
        * Arithmetic Operators
        * + -> Addition
        * - -> Substraction
        * * -> Multiplication
        * / -> Division
        * % -> Modulus(Reminder)
        */

        int a = 20;
        int b = 10;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);
        System.out.println("a+b");
        System.out.println(a%b);

    }
}
