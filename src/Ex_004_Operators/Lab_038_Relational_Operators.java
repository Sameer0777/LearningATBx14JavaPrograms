package Ex_004_Operators;

public class Lab_038_Relational_Operators {
    public static void main(String[] args) {

        /*
        * < -> Less than
        * <= -> Less than or equal to
        * > -> Greater than
        * >= -> Greater than or equal to
        * == -> Equals to
        * != -> Not equal to*/

        int a = 10;
        int b = 20;
        boolean c = a<b;
        System.out.println(c);

        int age = 24;
        int age1 = 26;
        boolean result = age>=age1;
        System.out.println(result);
    }
}
