package Ex_009_Switch;

public class Lab_094_Interview {
    public static void main(String[] args) {

        char ch = 'C';
        switch (ch){
            default:
                System.out.println("Alphabet");
            case 'A':
                System.out.println("65");
                break;
            case 'B' :
                System.out.println("66");
                break;
        }
    }
}
